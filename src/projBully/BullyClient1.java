    public void fazerEleicao() {
        System.out.println("Iniciando eleição...");
        this.emEleicao = true;
        boolean sentToHigher = false;

        synchronized (clientesAtivos) {
            for (int id : clientesAtivos) {
                if (id > this.myId) {
                    tcpClient.writeMessage("ELECTION|" + this.myId + "|");
                    sentToHigher = true;
                    break;
                }
            }
        }

        if (!sentToHigher) {
            System.out.println("Ninguém maior que eu. Eu sou o novo coordenador.");
            this.coordinatorId = this.myId;
            this.emEleicao = false;
            tcpClient.writeMessage("COORDINATOR|" + this.myId + "|");
        }
    }

    public void atualizarListaCliente(List<Integer> ids) {
        synchronized (clientesAtivos) {
            clientesAtivos.clear();
            clientesAtivos.addAll(ids);
        }
        System.out.println("Clientes ativos: " + ids);
    }

    public void receberEleicaoInfo(int senderId) {
        if (senderId < this.myId) {
            System.out.println("Recebi ELECTION de " + senderId + ". Respondendo ALIVE.");
            tcpClient.writeMessage("ALIVE|" + this.myId + "|");

            if (!this.emEleicao) {
                fazerEleicao();
            }
        }
    }

    public void receberAlive(int senderId) {
        if (this.emEleicao && senderId > this.myId) {
            System.out.println("Recebi ALIVE de " + senderId + ". Perdi a eleição.");
            this.emEleicao = false;
        }
    }

    public void recebeMensagemCoord(int winnerId) {
        System.out.println("Novo coordenador é: " + winnerId);
        this.coordinatorId = winnerId;
        this.emEleicao = false;

        javax.swing.SwingUtilities.invokeLater(new Runnable() {
            public void run() {
                jLabel4.setText(myId == winnerId ? "ADM" : "User");
                jLabel6.setText(winnerId >= 0 ? ("Coordenador: " + winnerId) : "Coordenador: Nenhum");
                jButton4.setEnabled(myId == winnerId ? true : false);
                jButton5.setEnabled(myId == winnerId ? true : false);
            }
        });
    }

    public void recebePingServer(long ts, int coordId) {
        if (this.myId == coordId && this.myId == this.coordinatorId) {
            tcpClient.writeMessage("PONG|" + this.myId + "|" + ts + "|");
        }
    }

    public void onServerTriggerElection() {
        int minId = this.myId;
        synchronized (clientesAtivos) {
            for (int id : clientesAtivos) {
                if (id < minId) {
                    minId = id;
                }
            }
        }
        if (this.myId == minId) {
            fazerEleicao();
        } else {
            System.out.println("TRIGGER_ELECTION recebido. Menor ID é " + minId + ". Aguardando ele iniciar.");
        }
    }

