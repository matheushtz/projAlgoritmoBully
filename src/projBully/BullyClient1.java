    public void onServerError(String code, String detail) {
        SwingUtilities.invokeLater(() -> {
            String msg;
            switch (code) {
                case "DUPLICATE_ID":
                    msg = (detail != null && !detail.isEmpty())
                            ? detail
                            : "ID já está em uso neste servidor. Escolha outro ID.";
                    break;
                default:
                    msg = "Erro do servidor: " + code;
                    if (detail != null && !detail.isEmpty()) {
                        msg += " — " + detail;
                    }
            }

            JOptionPane.showMessageDialog(
                    this, msg, "ERRO", JOptionPane.ERROR_MESSAGE
            );

            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
            jTextField3.setEditable(true);
            jLabel4.setText("User");
            jLabel6.setText("Coordenador: Nenhum");
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
        });

        try {
            if (tcpClient != null) {
                tcpClient.closeConnection();
            }
        } catch (IOException ignore) {
        }
    }

